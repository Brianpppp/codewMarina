package UF4.ACT2Herencia;

public class Media {
    private String nom;
    private int durada;

    public String getNom() {
        return nom;
    }

    public Media setNom(String nom) {
        this.nom = nom;
        return this;
    }

    public int getDurada() {
        return durada;
    }

    public Media setDurada(int durada) {
        this.durada = durada;
        return this;
    }
}
